
/*

Cookies are small pieces of data stored on a user's device by the web browser when visiting a website. They are used to remember information about the user between page visits or sessions.

    Key Features:
        Storage: Cookies store data like user preferences, login sessions, and shopping cart items.
        Expiration: Cookies have an expiration date after which they are automatically deleted.

        Types:
            Session cookies: Temporary and deleted when the browser is closed.
            Persistent cookies: Remain until they expire or are manually deleted.
            Secure cookies: Transmitted only over HTTPS.
            HttpOnly cookies: Accessible only by the server, not JavaScript.

    Common Uses:
        User authentication (keeping users logged in)
        Personalizing user experience (like remembering language preferences)
        Tracking user behavior for analytics and ads.

        */